#include <stdio.h>
/**
 * add - Adds two integers and prints the result
 *@a: user first input
 *@b: user second input
 * Return: Returns the value of addition
 *
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - Subtracts two integers and prints the result
 * @a: user first input
 * @b: user second input
 * Return: Returns the value of subtraction
 *
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * div - Divides two integers and prints the result
 * @a: user first input
 * @b: user second input
 * Return: Returns the value of division
 *
 */
int div(int a, int b)
{
        return (a / b);
}
/**
 * mul - Subtracts two integers and prints the result
 * @a: user first input
 * @b: user second input
 * Return: Returns the value of multiplication
 *
 */
int mul(int a, int b)
{
        return (a * b);
}
/**
 * mod - Subtracts two integers and prints the result
 * @a: user first input
 * @b: user second input
 * Return: Returns the modulus of the operation
 *
 */
int mod(int a, int b)
{
        return (a % b);
}

